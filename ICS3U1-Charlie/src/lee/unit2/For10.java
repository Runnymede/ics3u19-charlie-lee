/**
 *<br>A program that incorpates the for loop to
 *<br>make a counter starting from 1 to 10 and prints each number
 * <br>created on Oct 8th, 2019
 *<br>@author Charlie Lee
 */

public class For10 {

	public static void main(String[] args) {
	    int i;
		
		for (i=1; i<=10; i++)
	    {
	     System.out.println(i);
	    }

	}

}
